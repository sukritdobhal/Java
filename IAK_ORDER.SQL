CREATE SEQUENCE DBXNET.IAKORDER_SEQ INCREMENT BY 1 MINVALUE 790000 MAXVALUE 999999999999999999999999999 NOCYCLE CACHE 20 NOORDER ;

CREATE OR REPLACE EDITIONABLE TRIGGER "DBXNET"."IAKORDER_TRIGG" 
BEFORE INSERT ON "DBXNET"."IAK_D"
FOR EACH ROW
BEGIN
        SELECT
                IAKORDER_SEQ.NEXTVAL
        INTO
                :NEW.ORDAT_SEQUENCE
        FROM
                DUAL;
END;

ALTER TRIGGER "DBXNET"."IAKORDER_TRIGG" ENABLE;